package com.neeraj.forloop;

public class Lab202 {

	public static void main(String[] args) {
		char ch='A';
		for(int a=0;a<10;a++)
		{
			System.out.println("*");
			for(int b=0;b<=a;b++)
			{
				System.out.print(ch+"");
			}
			System.out.println("9");
			//System.out.println("Hello");
		}
	}

}
